            real_connect = sqlite3.connect
            traces: list[str] = []
            post_swap_snapshot: list[object] = []
            calls: list[tuple[object, bool]] = []

            def swap_after_preflight(path: Path) -> None:
                _preflight_schema_compatibility(path)
                with closing(real_connect(path)) as conn:
                    conn.execute("UPDATE schema_version SET version = 7")
                    conn.commit()
                post_swap_snapshot.append(_database_snapshot(path))

            def connect_spy(*args: object, **kwargs: object) -> sqlite3.Connection:
                calls.append((args[0], bool(kwargs.get("uri", False))))
                conn = real_connect(*args, **kwargs)
                if not kwargs.get("uri", False):
                    conn.set_trace_callback(traces.append)
                return conn

            with (
                patch(
                    "zeus.sqlite_db._preflight_schema_compatibility",
                    side_effect=swap_after_preflight,
                ),
                patch("zeus.sqlite_db.sqlite3.connect", side_effect=connect_spy),
                self.assertRaisesRegex(RuntimeError, "newer than supported"),
            ):
                SQLiteDatabase(database_path).connect()

            self.assertEqual(1, len(post_swap_snapshot))
            self.assertEqual(post_swap_snapshot[0], _database_snapshot(database_path))
            self.assertEqual(2, len(calls))
            self.assertTrue(calls[0][1])
            self.assertFalse(calls[1][1])
            self.assertTrue(any("schema_version" in sql for sql in traces))
            self.assertFalse(any(sql.lstrip().upper().startswith("PRAGMA") for sql in traces))
            self.assertFalse(any(sql.lstrip().upper().startswith("BEGIN") for sql in traces))
            self.assertFalse(any(sql.lstrip().upper().startswith("UPDATE") for sql in traces))


