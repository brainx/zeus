def _ddl_catalog(database_path: Path) -> tuple[tuple[str, str, str, str], ...]:
    placeholders = ", ".join("?" for _name in DDL_OBJECT_NAMES)
    with closing(sqlite3.connect(database_path)) as conn:
        rows = conn.execute(
            f"""
            SELECT type, name, tbl_name, hex(CAST(sql AS BLOB))
            FROM sqlite_master
            WHERE name IN ({placeholders})
            ORDER BY type, name
            """,
            DDL_OBJECT_NAMES,
        ).fetchall()
    return tuple((str(row[0]), str(row[1]), str(row[2]), str(row[3])) for row in rows)


def _seed_legacy_database(database_path: Path, version: int, *, with_bot: bool = False) -> None:
    if not 0 <= version <= 5:
        raise ValueError("legacy schema version must be between 0 and 5")
    manager = SchemaManager(SQLiteDatabase(database_path))
    with closing(sqlite3.connect(database_path)) as conn:
        conn.row_factory = sqlite3.Row
        conn.execute("BEGIN IMMEDIATE")
        conn.execute(BASE_BOTS_SCHEMA)
        if version >= 1:
            manager._ensure_restart_schema(conn)
        if version >= 2:
            manager._ensure_lifecycle_schema(conn)
        if with_bot:
            conn.execute(
                """
                INSERT INTO bots (
                    bot_id, template_id, display_name, profile_path, status, pid,
                    created_at, updated_at
                ) VALUES (
                    'coder', 'coding-bot', 'Coder', '/profiles/coder', 'running', 4321,
                    '2026-01-01T00:00:00+00:00', '2026-01-01T00:00:00+00:00'
                )
                """
            )
        if version >= 3:
            manager._migrate_v2_to_v3(conn)
        if version >= 4:
            manager._migrate_v3_to_v4(conn)
        if version >= 5:
            manager._migrate_v4_to_v5(conn)
        if version > 0:
            conn.execute(
                """
                CREATE TABLE schema_version (
                    version INTEGER NOT NULL
                )
                """
            )
            conn.execute("INSERT INTO schema_version (version) VALUES (?)", (version,))
        conn.commit()


def _database_snapshot(
    database_path: Path,
) -> tuple[
    str,
    tuple[str, ...],
    str,
    tuple[tuple[str, bool], ...],
]:
    digest = hashlib.sha256(database_path.read_bytes()).hexdigest()
    uri = f"{database_path.resolve().as_uri()}?mode=ro"
    with closing(_ORIGINAL_SQLITE_CONNECT(uri, uri=True)) as conn:
        dump = tuple(conn.iterdump())
        journal_mode = str(conn.execute("PRAGMA journal_mode").fetchone()[0])
    sidecars = tuple(
        (suffix, database_path.with_name(database_path.name + suffix).exists())
        for suffix in ("-wal", "-shm", "-journal")
    )
    return digest, dump, journal_mode, sidecars


class _TracingSQLiteDatabase(SQLiteDatabase):
    traces: ClassVar[list[tuple[int, str]]]

    def __init__(self, database_path: Path | str) -> None:
        super().__init__(database_path)
        self.traces = []

    def connect(self) -> sqlite3.Connection:
        conn = super().connect()
        connection_id = id(conn)
        conn.set_trace_callback(lambda sql: self.traces.append((connection_id, sql)))
        return conn


